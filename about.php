<h3>Retention of personal information</h3>
<br>

We will retain your personal information on our systems only for as long as we need to, for the success of the CyberSafe open source project and the programs that support CyberSafe. We keep contact information (such as mailing list information) until a user unsubscribes or requests that we delete that information from our live systems. If you choose to unsubscribe from a mailing list, we may keep certain limited information about you so that we may honor your request.

